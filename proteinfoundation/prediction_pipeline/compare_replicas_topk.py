"""
Compare top-1 template (by min pTM across AF2Rank m1/m2) and the corresponding
AF2Rank refined prediction:
- Two-replica mode: between two Proteina model replicas (--replica_a_dir and
  --replica_b_dir both provided).
- Self mode: between the first and second halves of samples generated by a
  single model (only --replica_a_dir provided). Split point = --max_sample_index;
  first half = [0, N), second half = [N, 2N).

For each target:
- Read per-target AF2Rank m1 / m2 score CSVs, inner-join on structure_file.
- Parse Proteina sample index from structure_file stem (last "_"-delimited token).
- Two-replica: filter to sample_index < max_sample_index (default 512) to keep
  the comparison apples-to-apples when one replica has more samples / different
  top_k. Self mode: partition into first/second halves using max_sample_index
  as the split point.
- Pick row with the highest min(ptm_m1, ptm_m2) from each side.
- "Corresponding prediction" = the AF2Rank variant (m1 or m2) with the higher
  pTM for that template, matching run_prediction_pipeline.step_collect_results.
- Run USalign -TMscore 5 -outfmt 2 on (a_template, b_template) and
  (a_prediction, b_prediction); record TM1/TM2/RMSD/Lali/L1/L2.
- Emit per-target CSV and an aggregate JSON summary.

Optional ground-truth mode (--use_ground_truth, requires --cif_dir):
- For each protein, select the "winner" side (higher min_ptm) and also run
  USalign of (winner_template, GT) and (winner_prediction, GT) to test whether
  replica convergence correlates with ground-truth prediction quality.
- Reports scatter plots of (replica a-vs-b TM) vs (winner-vs-GT TM) for both
  templates and predictions, in unfiltered and passing variants.
"""

