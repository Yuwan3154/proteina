def _slugify(label: str) -> str:
    """Lowercase, collapse non-alphanumerics to '_', strip leading/trailing '_'."""
    slug = re.sub(r'[^a-zA-Z0-9]+', '_', label).strip('_').lower()
    return slug


def parse_usalign_tsv(tsv_file):
    """Parse USalign output TSV file to extract TM1 scores."""
    tm_scores = []

    with open(tsv_file, 'r') as f:
        for line in f:
            if line.startswith('#'):
                continue
            line = line.strip()
            if not line or line.startswith('Warning'):
                continue
            parts = line.split('\t')
            if len(parts) >= 3:
                # Column 2 (index 2) is TM1
                tm1 = float(parts[2])
                tm_scores.append(tm1)

    return tm_scores


def load_template_search_results(all_protein_ids, template_aln_dir: Path):
    """
    Load template search results by parsing individual alignment files.
    For proteins without results (failed searches), set TM-scores to 0.0.
    """
    results = []

    # Get all .tsv files in template_aln directory
    tsv_files = {f.stem: f for f in template_aln_dir.glob("*.tsv")
                 if f.name != "tm_scores_results.tsv"}

    for protein_id in all_protein_ids:
        if protein_id in tsv_files:
            # Parse TM scores
            tm_scores = parse_usalign_tsv(tsv_files[protein_id])

            if tm_scores:
                results.append({
                    'protein_id': protein_id,
                    'template_top_1': tm_scores[0],  # First score (top-1 by sum_probs)
                    'template_top_5': max(tm_scores),  # Maximum score among all
                    'template_failed': False
                })
            else:
                # Empty file
                results.append({
                    'protein_id': protein_id,
                    'template_top_1': 0.0,
                    'template_top_5': 0.0,
                    'template_failed': True
                })
        else:
            # No result file (MSA search failed)
            results.append({
                'protein_id': protein_id,
                'template_top_1': 0.0,
                'template_top_5': 0.0,
                'template_failed': True
            })

    return pd.DataFrame(results)


def detect_scorer_mode(proteina_df: pd.DataFrame) -> str:
    """
    Detect whether the cross-protein summary was generated by AF2Rank or ProteinEBM.
    Returns 'af2rank' or 'proteinebm'.
    """
    if 'top_1_tm_ref_pred' in proteina_df.columns and 'top_5_tm_ref_pred' in proteina_df.columns:
        return 'af2rank'
    return 'proteinebm'


def load_and_merge_data(
    proteina_results_csv: Path,
    template_aln_dir: Path,
    af2rank_topk_csv: Path | None,
    list_csv: Path | None,
    list_id_column: str,
):
    """Load datasets, optionally filter by a list CSV, and merge on protein ID."""
    # Load Proteina results
    proteina_df = pd.read_csv(proteina_results_csv)

    # Detect scorer mode
    scorer_mode = detect_scorer_mode(proteina_df)
    print(f"Detected scorer mode: {scorer_mode.upper()}")

    if scorer_mode == 'proteinebm':
        if af2rank_topk_csv is None:
            print(
                "\nERROR: ProteinEBM scorer mode detected — the cross-protein summary does not contain\n"
                "  top_1_tm_ref_pred / top_5_tm_ref_pred (AF2 prediction quality columns).\n"
                "  These are only produced by the AF2Rank-on-ProteinEBM-top-k step.\n"
                "  Please re-run the pipeline with --af2rank_top_k > 0 and then supply the\n"
                "  resulting cross-protein summary via --af2rank_topk_csv.",
                file=sys.stderr,
            )
            sys.exit(1)

        topk_df = pd.read_csv(af2rank_topk_csv)
        missing_pred = [c for c in ('top_1_tm_ref_pred', 'top_5_tm_ref_pred') if c not in topk_df.columns]
        if missing_pred:
            print(
                f"\nERROR: --af2rank_topk_csv is missing required columns: {missing_pred}\n"
                f"  Found columns: {topk_df.columns.tolist()}\n"
                f"  Ensure the file was generated by generate_cross_protein_plots.py with\n"
                f"  --scorer af2rank_on_proteinebm_topk --af2rank_top_k > 0.",
                file=sys.stderr,
            )
            sys.exit(1)

        # Merge the AF2 prediction columns into the main ProteinEBM summary.
        # top_1/5_tm_ref_template come from the main ProteinEBM summary (all templates);
        # top_1/5_tm_ref_pred come from the af2rank_topk summary (top-k templates only).
        pred_cols = ['protein_id', 'top_1_tm_ref_pred', 'top_5_tm_ref_pred']
        proteina_df = proteina_df.merge(
            topk_df[[c for c in pred_cols if c in topk_df.columns]],
            on='protein_id',
            how='left',
        )
        print(f"Merged AF2Rank top-k pred columns from: {af2rank_topk_csv}")

    # Optional subset filter
    if list_csv is not None:
        list_df = pd.read_csv(list_csv)
        if list_id_column not in list_df.columns:
            print(
                f"\nERROR: --list_csv {list_csv} has no column '{list_id_column}'.\n"
                f"  Found columns: {list_df.columns.tolist()}\n"
                f"  Pass the correct column via --list_id_column.",
                file=sys.stderr,
            )
            sys.exit(1)
        subset_ids = set(list_df[list_id_column].values)
        print(f"Subset protein IDs from --list_csv ({list_id_column}): {len(subset_ids)}")
        proteina_df = proteina_df[proteina_df['protein_id'].isin(subset_ids)]
        print(f"Proteina results after subset filter: {len(proteina_df)}")
    else:
        print("No --list_csv provided — processing all proteins in --proteina_results_csv.")

    # Get all protein IDs from (filtered) Proteina data
    all_protein_ids = proteina_df['protein_id'].values

    # Load template search results (including failed ones with TM=0)
    template_df = load_template_search_results(all_protein_ids, template_aln_dir)

    # Merge on protein_id
    merged_df = pd.merge(
        template_df,
        proteina_df,
        on='protein_id',
        how='inner',
    )

    print(f"Template search results: {len(template_df)} proteins")
    print(f"  - Successful: {(~template_df['template_failed']).sum()}")
    print(f"  - Failed (TM=0): {template_df['template_failed'].sum()}")
    print(f"Merged dataset: {len(merged_df)} proteins")

    return merged_df


def create_scatter_plot(x, y, xlabel, ylabel, title, output_file, add_stats=True, filter_threshold=None):
    """Create a scatter plot with x=y diagonal line and statistics."""
    fig, ax = plt.subplots(figsize=(8, 8))

    # Apply filter if specified
    if filter_threshold is not None:
        mask = (x >= filter_threshold) | (y >= filter_threshold)
        x_plot = x[mask]
        y_plot = y[mask]
        title = f"{title}\n(Filtered: at least one value ≥ {filter_threshold})"
    else:
        x_plot = x
        y_plot = y

    # Scatter plot
    ax.scatter(x_plot, y_plot, alpha=0.6, s=50, edgecolors='black', linewidth=0.5)

    # Diagonal line (x=y)
    min_val = 0
    max_val = 1
    ax.plot([min_val, max_val], [min_val, max_val], 'r--', linewidth=2, label='x=y', alpha=0.7)

    if add_stats and len(x_plot) > 0:
        # Calculate statistics
        pearson_r, pearson_p = stats.pearsonr(x_plot, y_plot)
        spearman_r, spearman_p = stats.spearmanr(x_plot, y_plot)

        # # Paired t-test (two-sided)
        # t_stat, t_p = stats.ttest_rel(y_plot, x_plot)

        # # Wilcoxon signed-rank test (two-sided)
        # wilcoxon_stat, wilcoxon_p = stats.wilcoxon(y_plot, x_plot, alternative='two-sided')

        # Add correlation statistics text
        stats_text = f'Pearson r = {pearson_r:.3f} (p={pearson_p:.2e})\n'
        stats_text += f'Spearman ρ = {spearman_r:.3f} (p={spearman_p:.2e})'
        ax.text(0.05, 0.95, stats_text, transform=ax.transAxes,
                verticalalignment='top', bbox=dict(boxstyle='round', facecolor='white', alpha=0.8),
                fontsize=10)

        # # Add difference statistics
        # mean_diff = (y_plot - x_plot).mean()
        # diff_text = f'Mean Δ = {mean_diff:.4f}\n'
        # diff_text += f'Paired t-test: p={t_p:.2e}\n'
        # diff_text += f'Wilcoxon: p={wilcoxon_p:.2e}\n'
        # diff_text += f'N = {len(x_plot)}'
        # ax.text(0.05, 0.05, diff_text,
        #         transform=ax.transAxes, verticalalignment='bottom',
        #         bbox=dict(boxstyle='round', facecolor='wheat', alpha=0.8), fontsize=10)

    ax.set_xlabel(xlabel, fontsize=12)
    ax.set_ylabel(ylabel, fontsize=12)
    ax.set_title(title, fontsize=14, fontweight='bold')
    ax.legend(loc='lower right')
    ax.grid(True, alpha=0.3)
    ax.set_aspect('equal', adjustable='box')

    # Set limits to [0, 1]
    ax.set_xlim(0, 1)
    ax.set_ylim(0, 1)

    plt.tight_layout()
    plt.savefig(output_file, dpi=300, bbox_inches='tight')
    print(f"Saved: {output_file}")
    plt.close()

    if add_stats and len(x_plot) > 0:
        return {
            'pearson_r': pearson_r,
            'spearman_r': spearman_r,
            # 't_stat': t_stat,
            # 't_p': t_p,
            # 'wilcoxon_stat': wilcoxon_stat,
            # 'wilcoxon_p': wilcoxon_p,
            # 'mean_diff': mean_diff,
            'n_samples': len(x_plot)
        }
    return None


