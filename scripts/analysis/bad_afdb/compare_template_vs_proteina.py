#!/usr/bin/env python3
"""
Compare Template Search TM-scores vs Proteina Sampling TM-scores

This script creates scatter plots comparing TM-scores obtained from:
1. Template search protocol (hhsearch + structure alignment)
2. Proteina generative sampling protocol (both predicted and reference templates)

Note: Results assume template search was run with k=5 to match Proteina's top-5 sampling.

Two scorer modes are supported:
  - AF2Rank mode: the cross-protein summary CSV contains top_1_tm_ref_pred / top_5_tm_ref_pred
    (generated by the old full-AF2Rank pipeline).
  - ProteinEBM mode: the cross-protein summary CSV was generated with --scorer proteinebm and
    does NOT contain tm_ref_pred columns.  In this case --af2rank_topk_csv must point to the
    af2rank_on_proteinebm_topk cross-protein summary (produced with --af2rank_top_k > 0), which
    contains top_1_tm_ref_pred / top_5_tm_ref_pred for the top-k ProteinEBM-selected templates.

Subset filtering:
  - When --list_csv is omitted, every protein in --proteina_results_csv is processed.
  - When --list_csv is provided, results are filtered to IDs in that CSV (column controlled
    by --list_id_column, default "pdb").

Labelling:
  - --label "Monomers" appends " (Monomers)" to figure titles and "_monomer" to output
    filenames. Default empty = no suffix.

Usage examples:
  # Plain run (no subset filter, AF2Rank pipeline):
  python compare_template_vs_proteina.py \\
    --proteina_results_csv ~/proteina/inference/<run>/bad_afdb_cross_protein_analysis/cross_protein_summary_data.csv \\
    --output_dir ~/data/bad_afdb/comparison_template_vs_proteina

  # Monomer subset:
  python compare_template_vs_proteina.py \\
    --proteina_results_csv ~/proteina/inference/<run>/bad_afdb_cross_protein_analysis/cross_protein_summary_data.csv \\
    --list_csv ~/data/bad_afdb/pdb_70_cluster_reps_aligned_confidence_aggregate_monomer_length_50-640_tm-05.csv \\
    --label "Monomers" \\
    --output_dir ~/data/bad_afdb/comparison_template_vs_proteina_monomer

  # ProteinEBM pipeline (new):
  python compare_template_vs_proteina.py \\
    --proteina_results_csv ~/proteina/inference/<run>/bad_afdb_cross_protein_analysis/cross_protein_summary_data.csv \\
    --af2rank_topk_csv    ~/proteina/inference/<run>/bad_afdb_cross_protein_analysis/cross_protein_summary_data_topk.csv \\
    --output_dir ~/data/bad_afdb/comparison_template_vs_proteina
"""

