def build_comparisons(label: str):
    """Apply --label to title/filename of each base comparison."""
    title_suffix = f" ({label})" if label else ""
    name_suffix = f"_{_slugify(label)}" if label else ""
    comparisons = []
    for c in BASE_COMPARISONS:
        comparisons.append({
            'x_col': c['x_col'],
            'y_col': c['y_col'],
            'x_label': c['x_label'],
            'y_label': c['y_label'],
            'title': c['base_title'] + title_suffix,
            'output_base': c['base_output'] + name_suffix,
        })
    return comparisons


def main():
    """Main analysis function."""
    parser = argparse.ArgumentParser(
        description="Compare template search TM-scores vs Proteina sampling TM-scores"
    )
    parser.add_argument(
        '--proteina_results_csv', required=True,
        help='Path to the cross-protein summary CSV produced by generate_cross_protein_plots.py '
             '(e.g. .../bad_afdb_cross_protein_analysis/cross_protein_summary_data.csv)',
    )
    parser.add_argument(
        '--af2rank_topk_csv', default=None,
        help='Path to the af2rank_on_proteinebm_topk cross-protein summary CSV '
             '(required when --scorer=proteinebm was used in the pipeline, i.e. when '
             'top_1_tm_ref_pred is absent from --proteina_results_csv).',
    )
    parser.add_argument(
        '--list_csv', default=None,
        help='Optional CSV with the subset of protein IDs to process. When omitted, all '
             'proteins in --proteina_results_csv are processed.',
    )
    parser.add_argument(
        '--list_id_column', default='pdb',
        help='Column in --list_csv that contains protein IDs matching the "protein_id" '
             'column of the cross-protein summary (default: "pdb").',
    )
    parser.add_argument(
        '--template_aln_dir', default=str(DEFAULT_TEMPLATE_ALN_DIR),
        help=f'Directory of per-protein USalign .tsv files '
             f'(default: {DEFAULT_TEMPLATE_ALN_DIR}).',
    )
    parser.add_argument(
        '--output_dir', required=True,
        help='Output directory for plots.',
    )
    parser.add_argument(
        '--label', default='',
        help='Optional label appended to figure titles (as " (<label>)") and output '
             'filenames (as "_<slugified-label>"). Empty = no suffix.',
    )
    args = parser.parse_args()

    proteina_results_csv = Path(args.proteina_results_csv).expanduser()
    af2rank_topk_csv = Path(args.af2rank_topk_csv).expanduser() if args.af2rank_topk_csv else None
    list_csv = Path(args.list_csv).expanduser() if args.list_csv else None
    template_aln_dir = Path(args.template_aln_dir).expanduser()
    output_dir = Path(args.output_dir).expanduser()
    output_dir.mkdir(parents=True, exist_ok=True)
    label = args.label

    print("=" * 80)
    print("Comparing Template Search (k=5) vs Proteina Sampling TM-scores")
    if label:
        print(f"Label: {label}")
    print("=" * 80)
    print(f"Proteina results CSV : {proteina_results_csv}")
    if af2rank_topk_csv:
        print(f"AF2Rank top-k CSV    : {af2rank_topk_csv}")
    if list_csv:
        print(f"List CSV             : {list_csv} (id column: {args.list_id_column})")
    print(f"Template aln dir     : {template_aln_dir}")
    print(f"Output directory     : {output_dir}")
    print()

    # Load data
    df = load_and_merge_data(
        proteina_results_csv=proteina_results_csv,
        template_aln_dir=template_aln_dir,
        af2rank_topk_csv=af2rank_topk_csv,
        list_csv=list_csv,
        list_id_column=args.list_id_column,
    )

    if len(df) == 0:
        print("ERROR: No matching proteins found between datasets!")
        return

    comparisons = build_comparisons(label)

    print("\n" + "=" * 80)
    print("Creating comparison plots...")
    print("=" * 80)

    all_results = {}

    for idx, comp in enumerate(comparisons, 1):
        print(f"\n{idx}. {comp['title']}")

        # Full dataset
        results_full = create_scatter_plot(
            x=df[comp['x_col']],
            y=df[comp['y_col']],
            xlabel=comp['x_label'],
            ylabel=comp['y_label'],
            title=comp['title'],
            output_file=output_dir / f"{comp['output_base']}.png",
        )
        all_results[f"{comp['output_base']}_full"] = results_full

        # Filtered dataset (at least one value >= 0.5)
        results_filtered = create_scatter_plot(
            x=df[comp['x_col']],
            y=df[comp['y_col']],
            xlabel=comp['x_label'],
            ylabel=comp['y_label'],
            title=comp['title'],
            output_file=output_dir / f"{comp['output_base']}_filtered_0.5.png",
            filter_threshold=0.5,
        )
        all_results[f"{comp['output_base']}_filtered"] = results_filtered

    # Summary statistics
    label_tag = f" [{label}]" if label else ""

    print("\n" + "=" * 80)
    print(f"STATISTICAL ANALYSIS - FULL DATASETS{label_tag}")
    print("=" * 80)

    for comp in comparisons:
        key = f"{comp['output_base']}_full"
        if key in all_results and all_results[key]:
            print_significance_results(comp['title'], all_results[key])

    print("\n" + "=" * 80)
    print(f"STATISTICAL ANALYSIS - FILTERED DATASETS (≥0.5){label_tag}")
    print("=" * 80)

    for comp in comparisons:
        key = f"{comp['output_base']}_filtered"
        if key in all_results and all_results[key]:
            print_significance_results(f"{comp['title']} [Filtered]", all_results[key])

    # Additional analysis: Which method performs better?
    print("\n" + "=" * 80)
    print(f"METHOD COMPARISON SUMMARY - FULL DATASET{label_tag}")
    print("=" * 80)

    # Compare Template vs Proteina Predicted
    for k in [1, 5]:
        template_col = f'template_top_{k}'
        proteina_col = f'top_{k}_tm_ref_pred'

        better_proteina = (df[proteina_col] > df[template_col]).sum()
        better_template = (df[template_col] > df[proteina_col]).sum()
        equal = (df[template_col] == df[proteina_col]).sum()

        print(f"\nTop-{k} (Template vs Proteina Predicted):")
        print(f"  Proteina better: {better_proteina} ({100*better_proteina/len(df):.1f}%)")
        print(f"  Template better: {better_template} ({100*better_template/len(df):.1f}%)")
        print(f"  Equal: {equal}")
        print(f"  Mean Template: {df[template_col].mean():.4f} ± {df[template_col].std():.4f}")
        print(f"  Mean Proteina: {df[proteina_col].mean():.4f} ± {df[proteina_col].std():.4f}")
        print(f"  Median Template: {df[template_col].median():.4f}")
        print(f"  Median Proteina: {df[proteina_col].median():.4f}")

    # Compare Template vs Proteina Reference Template
    for k in [1, 5]:
        template_col = f'template_top_{k}'
        proteina_col = f'top_{k}_tm_ref_template'

        better_proteina = (df[proteina_col] > df[template_col]).sum()
        better_template = (df[template_col] > df[proteina_col]).sum()
        equal = (df[template_col] == df[proteina_col]).sum()

        print(f"\nTop-{k} (Template vs Proteina Ref Template):")
        print(f"  Proteina better: {better_proteina} ({100*better_proteina/len(df):.1f}%)")
        print(f"  Template better: {better_template} ({100*better_template/len(df):.1f}%)")
        print(f"  Equal: {equal}")
        print(f"  Mean Template: {df[template_col].mean():.4f} ± {df[template_col].std():.4f}")
        print(f"  Mean Proteina: {df[proteina_col].mean():.4f} ± {df[proteina_col].std():.4f}")
        print(f"  Median Template: {df[template_col].median():.4f}")
        print(f"  Median Proteina: {df[proteina_col].median():.4f}")

    print("\n" + "=" * 80)
    print("Analysis complete!")
    print("=" * 80)


if __name__ == "__main__":
    main()
